#include "../../include/so_long.h"

void	ft_putnbr(int n)
{
	if (n < 10)
	{
		n += 48;
		write(1, &n, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
